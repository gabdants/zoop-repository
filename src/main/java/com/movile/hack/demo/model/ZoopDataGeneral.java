package com.movile.hack.demo.model;

public class ZoopDataGeneral {

    String tokenCard;
    String buyer;
    String buyerCard;
    String clientCpf;

    public ZoopDataGeneral() {
    }

    public String getTokenCard() {
        return tokenCard;
    }

    public void setTokenCard(String tokenCard) {
        this.tokenCard = tokenCard;
    }

    public String getBuyer() {
        return buyer;
    }

    public void setBuyer(String buyer) {
        this.buyer = buyer;
    }

    public String getBuyerCard() {
        return buyerCard;
    }

    public void setBuyerCard(String buyerCard) {
        this.buyerCard = buyerCard;
    }

    public String getClientCpf() {
        return clientCpf;
    }

    public void setClientCpf(String clientCpf) {
        this.clientCpf = clientCpf;
    }
}
